#!/usr/bin/env bash

mkdir $1 && cd $1
npm init -y &>/dev/null
npm install --save-dev electron @electron-forge/cli &>/dev/null

sed -i 's/index\.js/main\.js/g' package.json
cat package.json

touch index.html
cat > index.html <<'EOT'
